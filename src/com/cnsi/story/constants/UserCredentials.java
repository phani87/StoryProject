package com.cnsi.story.constants;

public class UserCredentials {

	private String userName;
	private String pwd;
	private String userBusinessArea;
	private String userLastLogin;
	
	//getters and setters
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	public String getUserBusinessArea() {
		return userBusinessArea;
	}
	public void setUserBusinessArea(String userBusinessArea) {
		this.userBusinessArea = userBusinessArea;
	}
	public String getUserLastLogin() {
		return userLastLogin;
	}
	public void setUserLastLogin(String userLastLogin) {
		this.userLastLogin = userLastLogin;
	}
	
}
